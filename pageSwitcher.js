function changeLang(trg) {
    if (trg == "cy") {
        location.href = "../cy/";
    }
    if (trg == "cy frm 2") {
        location.href = "../../cy/";
    }
    if (trg == "cy/py") {
        location.href = "../cy/python";
    }
    if (trg == "cy/py/3") {
        location.href = "../../../cy/python";
    }
    if (trg == "cy/py/pygame") {
        location.href = "../../cy/python/pygame";
    }
    if (trg == "en") {
        location.href = "../en/";
    }
    if (trg == "en frm 2") {
        location.href = "../../en/";
    }
    if (trg == "en/py") {
        location.href = "../en/python";
    }
    if (trg == "en/py/3") {
        location.href = "../../../en/python";
    }
    if (trg == "en/py/pygame") {
        location.href = "../../en/python/pygame";
    }
}